#include <unistd.h>
/**
 * _isalpha - Say if a letter
 * @c: Character entered
 *
 * Return: 0 if c is letter
 * 1 otherwise
 */
int _isalpha(int c);
/**
 * _putchar - writes the character c to stdout
 * @c: The character to print
 *
 * Return: On success 1.
 * On error, -1 is returned, and errno is set appropriately.
 */
int _putchar(char c);
