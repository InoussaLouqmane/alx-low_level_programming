#include <unistd.h>
/**
 * _islower - Say if a character
 * is lowercase or not
 * @c: Character entered
 *
 * Return: 0 if c is lowercase
 * 1 otherwise
 */
int _islower(int c);
/**
 * _putchar - writes the character c to stdout
 * @c: The character to print
 *
 * Return: On success 1.
 * On error, -1 is returned, and errno is set appropriately.
 */
int _putchar(char c); 
